      fullfile(MATLAB_FUNCTION_PATH, 'libstruct', 'numfields'); ...
      fullfile(getenv('FEXFUNCTIONPATH'), 'libarrays', 'foreach'); ...
      }];
end

for m = 1:numel(removePaths)
   subpaths = subpaths(keep(subpaths, removePaths{m}));
end

