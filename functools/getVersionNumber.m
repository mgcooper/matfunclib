function versionNumber = getVersionNumber(releaseName)

   % Define the mapping between version numbers and release names
   releaseMap = containers.Map(...
      {'R2023a', 'R2022b', 'R2022a', 'R2021b', 'R2021a', 'R2020b', 'R2020a', ...
      'R2019b', 'R2019a', 'R2018b', 'R2018a', 'R2017b', 'R2017a', 'R2016b', ...
      'R2016a', 'R2015b', 'R2015a', 'R2014b', 'R2014a', 'R2013b', 'R2013a', ...
      'R2012b', 'R2012a', 'R2011b', 'R2011a', 'R2010b', 'R2010a', 'R2009b', ...
      'R2009a', 'R2008b', 'R2008a', 'R2007b', 'R2007a', 'R2006b', 'R2006a', ...
      'R2005b', 'R2005a'} , ...
      {'9.14', '9.13', '9.12', '9.11', '9.10', '9.9', '9.8', ...
      '9.7', '9.6', '9.5', '9.4', '9.3', '9.2', '9.1', ...
      '9.0', '8.6', '8.5', '8.4', '8.3', '8.2', '8.1', ...
      '8.0', '7.14', '7.13', '7.12', '7.11', '7.10', '7.9', ...
      '7.8', '7.7', '7.6', '7.5', '7.4', '7.3', '7.2', ...
      '7.1', '7.0'});

   % Ensure R is uppercase and the semester is lowercase.
   releaseName = [upper(releaseName(1)) releaseName(2:end-1) lower(releaseName(end))];

   % Lookup the release name for the given version number
   if isKey(releaseMap, releaseName)
      versionNumber = releaseMap(releaseName);
   else
      error('Release name %s is not recognized.', releaseName);
   end
end
